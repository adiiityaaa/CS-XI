#include <iostream>
using namespace std;

int main() {
 cout<<"Size of Int is "<<sizeof(int)<<" byte(s)."<<endl;
 cout<<"Size of Short Int is "<<sizeof(short int)<<" byte(s)."<<endl;
 cout<<"Size of Long Int is "<<sizeof(long int)<<" byte(s)."<<endl;
 cout<<"Size of Char is "<<sizeof(char)<<" byte(s)."<<endl;
 cout<<"Size of Float is "<<sizeof(float)<<" byte(s)."<<endl;
 cout<<"Size of Double is "<<sizeof(double)<<" byte(s)."<<endl;
 cout<<"Size of Boolean is "<<sizeof(bool)<<" byte(s)."<<endl;
}
//output:
//Size of Int is 4 byte(s).
//Size of Short Int is 2 byte(s).
//Size of Long Int is 4 byte(s).
//Size of Char is 1 byte(s).
//Size of Float is 4 byte(s).
//Size of Double is 8 byte(s).
//Size of Boolean is 1 byte(s).